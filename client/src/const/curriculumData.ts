// Helper function to get lessons by level
export const getLessonsByLevel = (level: CEFRLevel): CurriculumLesson[] => {
  return CURRICULUM_LESSONS.filter(lesson => lesson.cefrLevel === level);
};

// Helper function to get level info
export const getLevelInfo = (code: CEFRLevel): CEFRLevelInfo | undefined => {
  return CEFR_LEVELS.find(level => level.code === code);
};

// Get total lessons count per level
export const getLessonCountByLevel = (): Record<CEFRLevel, number> => {
  const counts: Record<CEFRLevel, number> = { A1: 0, A2: 0, B1: 0, B2: 0, C1: 0, C2: 0 };
  CURRICULUM_LESSONS.forEach(lesson => {
    counts[lesson.cefrLevel]++;
  });
  return counts;
};
